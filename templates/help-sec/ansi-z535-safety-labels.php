	<p class="h4">Safety Label: Letter Size Legibility</p>
	<p>Determining the safe viewing distance of a label depends upon many factors, including <br />
		&bull; Lighting, background, letter type and visual acuity. <br />
		Other factors to consider in determining label size, include: <br />
		&bull; The complexity of the message, the reaction time necessary, and the angle the label will be seen from. In many situations it is better to use multiple labels that are strategically positioned instead of one large one.</p>
	<p>Many times letter size may need to be larger than the values shown on the graphic for a variety of reasons:<br />
		1. To make the label more noticeable from other information displayed in the area. <br />
		2. To enable legibility under low light, or other poor viewing conditions. <br />
		3. To alert persons sooner than the minimum safe viewing distance. <br />
		4. To convey special importance for portions of the message. <br />
		5. To improve legibility for persons who have vision problems with small text.</p>
