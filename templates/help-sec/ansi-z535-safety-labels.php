<div class="help-term prepend-top">
	<p class="h4">Use of Safety Symbols</p>
	<p>ANSI Z535.4 2002 encourages the use of safety symbols that communicate a comparable message to the worded message on the sign or label. The safety symbol when selected properly, should identify: <br />
		&bull; The hazard, <br />
		&bull; Identify a means of avoiding the hazard, <br />
		&bull; Or identify the consequences of not avoiding the hazard. The safety symbol should clarify or reinforce the worded message on the label.</p>
	<p>There are four types of safety symbols used on ANSI Z535.4 2002 compliant product safety labels. When using safety symbols to comply with ANSI, the symbol can be black on a white background, other colors may be used, like safety red for fire related symbols, and so on.</p>
	<p>To comply with both ANSI Z535.4 and ISO 3864, then the following applies, ISO 3864 2002 standards required that all of the safety symbols used on international product safety labels be within a surround shape, yellow equilateral triangle with black inner border for hazard alerting symbols, blue circle for mandatory actions, and red circle with 45&deg; degree slash for prohibition symbols. ANSI Z535.4 2002 does not required the use of a yellow equilateral triangle or the mandatory surround shape but, it does recognize it and permits their use.</p>
</div>
<div class="help-term prepend-top">
