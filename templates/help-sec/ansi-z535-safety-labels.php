	<p class="h4">ANSI Z535.4 Safety Labels Construction</p>
	<p>ANSI Z535.4 Product Safety Signs &amp; Labels specifies performance requirements for the design, application, use and placement of safety labels and signs that are intended to identify hazards that a person may encounter while operating or servicing a variety of products.</p>
</div>
<div class="help-term prepend-top">
	<p class="h4">ANSI Z535.4 Safety Labels Description</p>
	<p>Adhesive Backed Polyester Labels have been designed to comply with ANSI Z535.4 2002 &amp; 2007 Product Safety Label Standards. Labels are screen printed with UV resistant inks and over laminated with Clear Polyester to ensure a high degree of chemical, abrasion and heat resistance</p>
</div>
<div class="help-term prepend-top">
	<p class="h4">ANSI Z535.4 Safety Labels Material</p>
	<p>2 Mil White Polyester with Hi-Performance pressure sensitive Acrylic Adhesive backing (UL&reg; File No. MH10170 / CSA File No. 099241 L000) 1 Mil Clear Polyester film coated with a clear pressure sensitive acrylic adhesive (UL&reg; File No. MH10170 / CSA File No. 099241 L000)</p>
	<p><strong>Printed Image:</strong> Printed image is sealed between 2 layers of polyester. Polyester offers superior chemical, abrasion and temperature resistance to vinyl labels, image is screen printed using UV resistant inks.</p>
