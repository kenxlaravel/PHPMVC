<div class="help-term prepend-top">
	<p class="h4">Safety Label Format</p>
	<p><strong>Message Panel:</strong> the message panel of any new ANSI Z535.4 2007 compliant sign or label must contain, <br />
		&bull; Identification of the hazard, <br />
		&bull; Identification of a means to avoid the hazard, <br />
		&bull; And the consequences of not avoiding the hazard.</p>
	<p>The order that the information appears in the message panel is flexible and should be determined by logical factors related to avoiding an injury, such as: <br />
		&bull; The target audiences knowledge of the hazard, <br />
		&bull; And the reaction time to avoid the hazard.</p>
	<p>In addition, we recommend when formatting the message panel to consider the following practices: <br />
		- Use headline style text <br />
		- Use active voice statements <br />
		- Avoid prepositional phrases <br />
		- Use left justification of the text except on one line messages <br />
		- Use upper and lower case letters</p>
	<p>Click on this link on view &amp; Print Safety Labels: <a href="http://www.safetysign.com/content/PDF/LabelSizeChart.pdf" target="_blank">Brimar's Standard Label Sizes</a></p>
