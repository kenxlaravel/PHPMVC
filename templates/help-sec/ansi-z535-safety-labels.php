	<p class="h4">Selecting the Right Safety Header</p>
	<p><strong>Hazard Classification: </strong>The first element to consider when designing or selecting a product safety label is to determine the likelihood and severity of a potential injury if a person does not follow the instructions contained on the safety label or sign. </p>
	<p>There are four Signal Words or Hazard Classifications used in the design of an ANSI Z535.4 2007 compliant safety label: &ldquo;<strong>DANGER</strong>&rdquo;, &ldquo;<strong>WARNING</strong>&rdquo;, &ldquo;<strong>CAUTION</strong>&rdquo; and &ldquo;<strong>NOTICE</strong>&rdquo;</p>
