<div class="help-term prepend-top">
	<p class="h4">Safety Label: Recommended Label Placement on Equipment</p>
	<p>It is well recognized that in many instances, well designed and properly placed safety labels can increase operator and maintenance personnel awareness of the inherent hazards involved when working with machinery<strong>.</strong></p>
	<p>When installing labels on equipment we recommend to keep in mind the following criteria: <br />
		&bull; Label needs to be readily visible to the target audience (personnel that may be in contact with the hazard) <br />
		&bull; Label needs to alert the viewer to the hazard in time to take appropriate action. <br />
		&bull; Labels are required to be applied on or adjacent to the area where the hazard is present. <br />
		&bull; Labels are required to be permanently affixed to the equipment. <br />
		&bull; Insure that the surface to which the label(s) will be applied on is clean and free of dirt, dust, grease and moisture. If the surface is painted, insure that the paint is dry. <br />
		&bull; Press the label on after applying it to the surface. One or more passes over with a rag or roller will ensure that the label is completely glued on the surface and will not fall off.</p>
