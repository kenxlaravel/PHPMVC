<div class="help-term prepend-top" id="cart-later">
	<p class="h4">Can I save a Shopping Cart for later? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">Yes, you can. You can add items to your cart at any time. When you are ready to save your cart, you must sign into your account. Once you are signed in, just click the "Save Cart" button on the Shopping Cart. You can then name your cart and add optional notes. If you don't have an account, you'll be prompted to create one before you can save.</p>

</div>
<hr>
<div class="help-term prepend-top" id="how-to-save">
	<p class="h4">How do I save a Shopping Cart? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">If you are signed in and have items in your shopping cart, simply click the "Save Cart" button on the Shopping Cart page. You can then name your cart and add optional notes. If you don't have an account, you'll be prompted to create one before you save. You can add items to your cart without being signed in. If you have already saved a cart with that name, you will be prompted to cancel and enter a new name, add the cart items to a previously saved cart, or overwrite the saved cart with your new cart.</p>
	<img src="/images/help-elements/save-cart.png" class="append-bottom-20" />


</div>
<hr>
<div class="help-term prepend-top" id="what-if-duplicate">
	<p class="h4">What if I duplicate my Saved Cart name? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">If you have already saved a cart with that name, you will be prompted to cancel and enter a new name, add the cart items to a previously saved cart, or overwrite the saved cart with your new cart.</p>
	<img src="/images/help-elements/save-conflict.png" class="append-bottom-20" />


</div>
<hr>
<div class="help-term prepend-top" id="where-are-buttons">
	<p class="h4">Where are my Save Cart and Load Cart buttons? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">You must be signed in to see the Save Cart or Load Cart buttons, though you can add items to your cart at any time. If you don't have an account, you'll be prompted to create one before you can save a cart. Once you are signed in you will be able to see the Save Cart button on the topp of the Shopping Cart page as well as on your My Account page. You must save at least one cart to see the Load Cart button as well. </p>
	<img src="/images/help-elements/signed-out.png" class="append-bottom-20" />

</div>
<hr>
<div class="help-term prepend-top" id="how-to-view">
	<p class="h4">How can I view my Saved Carts? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">If you are signed in, simply visit your My Account page and you will be able to view all of your saved carts, as well as the details for each cart. You can also load a saved cart from your My Account page or the Shopping Cart.</p>
	<img src="/images/help-elements/cart-details.png" class="append-bottom-20" />

</div>
<hr>
<div class="help-term prepend-top" id="how-to-load">
	<p class="h4">How do I load a saved cart? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">You must have a previously saved cart and also be signed in to your account. You can then load a recently saved cart from the Shopping Cart page. You can also visit your My Account page and click the Load Cart button of the desired cart. If you have items in your cart when you try to load another cart, you will be prompted to add the saved cart to your current cart, replace your current cart with your saved cart, or cancel loading. If you cancel, you can save your current cart before trying again.</p>
	<img src="/images/help-elements/load-cart.png" class="append-bottom-20" />

</div>
<hr>
<div class="help-term prepend-top" id="what-if-load">
	<p class="h4">What if I try to load a cart and already have items in my cart? <span class="right-side block"><a href="#" class="normal-weight underline small-text">Back To Top</a></span></p>
	<p class="append-bottom-20">If you have items in your cart already, you will be prompted to add the saved cart to your current cart, replace your current cart with your saved cart, or cancel loading. If you cancel, you can save your current cart before trying to load your desired saved cart.</p>
	<img src="/images/help-elements/merge-conflict.png" class="append-bottom-20" />

