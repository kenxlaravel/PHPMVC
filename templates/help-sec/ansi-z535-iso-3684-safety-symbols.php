	<p>There are 2 basic types of safety symbols for use on safety signs, labels and tags: hazard alerting and hazard avoidance.</p>
	<p><strong>Hazard Alerting Symbols:</strong> A well developed hazard description pictorial should clearly identify the hazard and portray the potential consequences of a failure to follow instructions.</p>
