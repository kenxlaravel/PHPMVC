	<p class="h4">Symbol Classification: Hazard Alert Symbols</p>
	<p><strong>Hazard Alerting:</strong> This type of safety symbol conveys information primarily related to the nature of hazards. Like, hazard description, how to avoid the hazard and sometimes the consequences of not avoiding the hazard.</p>
	<p>If a surround shape is desired, the hazard alerting symbol should be drawn within a yellow equilateral triangle. The yellow equilateral triangle is required on safety alert symbols used on ISO compliant product safety labels. <em>The image below shows some examples from our Safety Symbol Database</em>.</p>
