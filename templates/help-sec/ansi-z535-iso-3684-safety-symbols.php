	<p class="h4">Symbol Classification: Prohibition Symbols</p>
	<p><strong>Prohibition Symbols:</strong> This type of safety symbol conveys actions that should not be taken. This symbol consists of a red circular band with a 45&deg; diagonal red band from upper left to lower right. The symbol contains a black image within the red band on a white background. <em><strong>Prohibition symbols are mandatory for use on ISO compliant product safety labels.</strong></em> <em>The image below shows some examples from our Safety Symbol Database.</em></p>
	<p><img src="/images/ANSI04.png" alt="Safety Symbols Classification" width="424" height="66" /></p>
</div>
<div class="help-term prepend-top">
	<p class="h4">Symbol Classification: Mandatory Symbols</p>
	<p><strong>Mandatory Action Symbols:</strong> This type of safety symbol conveys actions that should be taken to avoid hazards. If a surround shape is desired, the symbol consist of a white image within a solid blue or black circular surround shape. <strong><em>The blue surround shape is required on ISO compliant product safety labels that contain mandatory action symbols</em></strong>. <em>The image below shows some examples from our Safety Symbol Database.</em></p>
