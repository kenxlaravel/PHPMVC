<div class="help-term prepend-top">
	<p class="h4">Use of Safety Symbols in conformance with ANSI Z535 &amp; ISO 3864-2</p>
	<p>ANSI Z535 encourages the use of safety symbols that communicate a comparable message to the worded message on the sign or label. The safety symbol when selected properly, should identify:</p>
	<ul class="with-bullets">
		<li><strong>The hazard</strong></li>
		<li><strong>Identify a means of avoiding the hazard </strong></li>
		<li><strong>Or identify the consequences of not avoiding the hazard.</strong></li>
	</ul>
	<p>The safety symbol should clarify or reinforce the worded message on the label. When safety symbols are being used they should be as simple as possible and should contain only essential details. They should be easy to learn and to recognize. <br />
		There are four types of safety symbols described in ANSI Z535.5 2007. </p>
	<ul class="with-bullets">
		<li><strong>Hazard Alerting</strong></li>
		<li><strong>Mandatory Action </strong></li>
		<li><strong>Prohibition </strong></li>
		<li><strong>Information</strong></li>
	</ul>
	<p>When using safety symbols to comply with ANSI, the symbol can be black on a white background, other colors may be used, like safety red for fire related symbols, green for emergency equipment, etc. To comply with both ANSI Z535.4 and ISO 3864 Standards simultaneously, then, the following applies, ISO 3864 2002 standards require that all of the safety symbols used on international product safety labels be within a surround shape, yellow equilateral triangle with black inner border for hazard alerting symbols, blue circle for mandatory actions, and red circle with 45&deg; degree slash for prohibition symbols. ANSI Z535.4 2007 does not required the use of a yellow equilateral triangle or the mandatory surround shape but, it does recognize it and permits their use.</p>
</div>
