<div class="help-term prepend-top">
	<p class="h4">Signal Words Revisions</p>
	<p>The 2002 revision stated that the signal words for product safety labels were: <strong>DANGER</strong>, <strong>WARNING, CAUTION</strong>. Where <strong>CAUTION</strong> could be used for property-damage-only accidents when used without the triangle and exclamation mark.</p>
	<p><img src="/images/ANSIH01.png" alt="ANSI Z535.4 Safety Labels" width="325" height="50" /><br />
		<strong>DANGER</strong>: Indicates an imminently hazardous situation which, if not avoided, will result in death or serious injury. Safety Signs identified by the signal word <strong>DANGER</strong> should be used sparingly and only for those situations presenting the most serious hazards.</p>
	<p><img src="/images/ANSIH02.png" alt="ANSI Z535.4 Safety Labels" width="326" height="50" /><br />
		<strong>WARNING</strong>: Indicates a potentially hazardous situation which, if not avoided, will result in death or serious injury. Hazards identified by the signal word <strong>WARNING</strong> present a lesser degree of risk of injury or death than those identified by the signal word <strong>DANGER</strong>.</p>
	<p><img src="/images/ANSIH03.png" alt="ANSI Z535.4 Safety Labels" width="424" height="50" /><br />
		<strong>CAUTION</strong>: Indicates a hazardous situation, which if not avoided, could result in minor or moderate injury. It may also be used without the safety alert symbol as an alternative to “<strong>NOTICE</strong>”</p>
	<p><img src="/images/ANSIH04.png" alt="ANSI Z535.4 Safety Labels" width="424" height="42" /><br />
		<strong>NOTICE</strong>: is the preferred signal word to address practices not related to personal injury. The safety alert symbol should not be used with this signal word. As an alternative to “<strong>NOTICE</strong>”, the word “<strong>CAUTION</strong>” without the safety alert symbol may be used to indicate a message not related to personal injury.</p>
	<p><img src="/images/ANSIH05.png" alt="ANSI Z535.4 Safety Labels" width="424" height="119" /><br />
		<strong>Safety instructions or safety equipment</strong> location signs indicate general instructions relative to safe work practices, reminders of proper safety procedures, or the location of safety equipment.</p>
</div>
