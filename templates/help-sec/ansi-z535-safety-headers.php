	<p class="h4">Selecting the right header for your label/sign</p>
	<p>The first element to consider when designing a product safety label is to determine the likelihood and severity of a potential injury if a person does not follow the instructions contained on the safety label or sign.</p>
	<p>For product identification there are four Signal Words or Hazard Classifications used in the design of an ANSI Z535.4 2007 compliant safety label: “<strong>DANGER</strong>”, “<strong>WARNING</strong>”, “<strong>CAUTION</strong>” or “<strong>NOTICE</strong>”</p>
	<p>When more than one hazard exists on a product it is permissible to use one label, provided the information addresses each hazard and the signal word selected is based on the greatest level of hazard.</p>
	<p>The Safety Alert Symbol is used to warn of a potential personal injury hazard. This symbol is used in conjunction with the signal word or can be used alone. This symbol should be used on both product safety labels and in literature which can describe the potential hazard in greater detail.</p>
	<p><img src="/images/ANSIR12.png" alt="ANSI Z535.4 Safety Labels" width="424" height="232" /></p>
