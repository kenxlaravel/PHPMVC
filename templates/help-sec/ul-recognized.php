    <p class="h4">UL&reg; Recognized Components from SafetySign.com</p>
    <p>Most of our machine safety labels, categorized by UL as UL Marking and Labeling Systems – Components, have earned the UL Recognized Component Mark. This mark signifies that our labels are intended for use as components of complete equipment, which is then submitted for UL investigation. It also signifies that our labels have been evaluated by UL for use on a variety of application surfaces under varying conditions.</p>
    <p>The basic standard used to investigate products in the UL Marking and Labeling Systems – Component category is UL 969:</p>
    <p class="ul-help-list">1.1 These requirements cover adhesive attached labels for use as nameplates or markers; bearing information, instructions, or identification. An adhesive for a label may be pressure sensitive, heat activated, or solvent activated. These labels are intended to be used by manufacturers for application to their products at their place of manufacture.</p>
    <p class="ul-help-list">1.2 These requirements also cover unprinted materials, such as face stocks, label stocks, over-laminations, laminating adhesives, and inks used by label printers to produce labels.</p>
    <p class="ul-help-list">1.3 These requirements apply to marking and labeling systems used on complete devices, appliances, or equipment. The acceptability of a system in a particular application is to be judged under the applicable requirements in the standard covering the device, appliance, or equipment on which the system is used.</p>
    <p class="ul-help-list">1.4 Marking and labeling systems are evaluated for application to specific surface materials that are essentially smooth and flat unless another surface configuration is specified by the manufacturer.</p>
    <p class="small-text">Source: <a href="http://ulstandardsinfonet.ul.com/scopes/scopes.asp?fn=0969.html">Marking and Labeling Systems UL 969</a></p>
    <p>Labels categorized by UL as UL Marking and Labeling Systems – Components, are intended to provide warnings, instructions, and other information necessitated by marking requirements. The following conditions are to be accepted when these components are employed in end-use equipment unless otherwise specified in the individual Recognitions:</p>
    <p class="ul-help-list">1.  Environmental Conditions: These labels are intended for use indoors, where exposure to high humidity or occasional exposure to water can be expected. Labels suitable only for indoor, dry-location use or for additional exposure conditions, such as outdoor use, gasoline splashing, etc., are identified in the individual Recognitions. Labels for which no minimum temperature rating is indicated are considered suitable for use in ambient temperatures down to 0°C (32°F).</p>
    <p class="ul-help-list">2.  Application Surfaces: These labels are suitable for application to smooth, flat surfaces subjected to the temperatures and conditions indicated.</p>
    <ol class="lower-alpha-list">
        <li>Painted surfaces on metal substrates. Labels suitable for use on a painted metal surface may also be considered suitable for use on a painted plastic surface where the heat stability and compatibility of the paint with the plastic substrate have been found to be acceptable for the application. Labels specified as suitable for use on a painted plastic are also considered suitable for use on painted metal. Labels suitable for application to two or more painted surfaces are considered suitable for blends of those paints, with Conditions of Acceptability common to the individual components in the blend.</li>
        <li>Labels suitable for application to a generic plastic surface are also considered suitable for application to filled or reinforced plastic of the same generic type. Labels suitable for application to two or more plastic surfaces are considered suitable for blends of those plastics, with Conditions of Acceptability common to the individual components in the blend.</li>
        <li>Labels suitable for use on a metal surface, such as aluminum, are considered suitable for use on alloys of that metal, such as aluminum/magnesium alloys. Labels suitable for use on aluminum are also considered suitable for use on anodized and die-cast aluminum surfaces.</li>
        <li>Labels indicated as suitable for use on a textured surface may be subject to further investigation to determine suitability for use on a particular textured end-product surface.</li>
        <li>Labels indicated as suitable for use on porcelain have been investigated on glazed porcelain.</li>
    </ol>
    <p class="ul-help-list">3. The labels have not been investigated for additional printing using hand implements, such as pen or pencil.</p>
    <p class="ul-help-list">4. The labels have not been investigated for non-transferability or for electrical or flammability properties.</p>
    <p class="ul-help-list">5. Labels that have additionally been investigated for use on specific end products are so indicated in the individual Recognitions.</p>
    <p class="ul-help-list">6. The labels are intended to be applied with sufficient pressure to provide uniform and complete contact with the surface.</p>

