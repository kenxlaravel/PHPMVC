<div class="help-term prepend-top-20">
	<p class="h4">3) What height do I need to mount my stop sign?</p>
	<div class="span-17 prepend-top append-bottom help-page-offset">
		<p class="bold font-14 first-pad">MOST COMMON APPLICATION</p>
			<p class="p-block"><span class="bold">In business, commercial, or residential areas (Fig. A):</span> The minimum height, measured vertically from the bottom of the sign to the top of the curb, or in the absence of curb, measured vertically from the bottom of the sign to the elevation of the near edge of the traveled way, of signs installed at the side of the road where the view of the sign might be obstructed, shall be 7 feet.<br><span class="bold">Signs installed above sidewalks:</span> The minimum height, measured vertically from the bottom of the sign to the sidewalk, shall be 7 feet.</p>
		<img src="/images/help-elements/stopsigns-helppage3.png">
	</div>
	<div class="span-17 append-bottom">
		<div class="span-8 prepend-top-20">
			<p><span class="bold">In rural areas (Fig. B):</span> The minimum height, measured vertically from the bottom of the sign to the elevation of the near edge of the pavement, shall be 5 feet.</p>
		</div>
		<div class="span-9 last"><img src="/images/help-elements/stopsigns-helppage2.png"></div>
	</div>

	<div class="span-17 append-bottom">
		<div class="span-8 prepend-top-20">
			<p><span class="bold">Overhead signs (Fig. C):</span> Shall provide a vertical clearance of not less than 17 feet to the sign, light fixture, or sign bridge over the entire width of the pavement and shoulders except where the structure on which the overhead signs are to be mounted or other structures along the roadway near the sign structure have a lesser vertical clearance.</p>
		</div>
