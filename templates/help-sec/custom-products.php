	<p class="h4">Do Not Use the &quot;Back&quot; Browser Button</p>
	<p>1. Please note, using the browser's &quot;<strong>Back</strong>&quot; button or navigating away from the &quot;Custom Tool&quot; will lose any change you made to your design, we recommend that you design your sign and then click the &quot;<strong>Continue To Save or Add To Cart</strong>&quot; button. From there you can save the design for later or add it to the cart.<br />
		<img src="/images/Custom03.png" alt="Custom Sign Tool" width="399" height="121" /></p>
	<p>2. If you add the design to the cart, but do not checkout, your custom design will likely no longer be in your shopping cart next time you come back to the site.</p>
	<p>3. If you save your shopping cart, your custom design will also be saved, as part of that shopping cart.</p>
